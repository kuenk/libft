#include "libft.h"

void    ft_putnbr(int nb)
{
    char    aux;

    if (nb == -2147483648)
        write (1, "-2147483648", 11);
    else
    {
        if (nb < 0)
        {
            nb = nb * -1;
            write (1, "-", 1);
        }
        if (nb < 10)
        {
            aux = nb + '0';
            write (1, &aux, 1);
        }
        else
        {
            ft_putnbr(nb / 10);
            aux = nb % 10 + '0';
            write(1, &aux, 1);
        }
    }
}